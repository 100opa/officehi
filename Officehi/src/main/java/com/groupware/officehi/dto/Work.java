package com.groupware.officehi.dto;


public class Work {

	private Long id;
	private String date;
	private String arrival;
	private String leave;
	
	public Work() {}

	public Work(Long id, String date, String arrival, String leave) {
		super();
		this.id = id;
		this.date = date;
		this.arrival = arrival;
		this.leave = leave;
	}
	
	
	
	
	
}
